INIMOUSE:
    MOV AX,00H
    INT 33H
    MOV AX,01H
    INT 33H
    
APUNTADOR:
    MOV AX,03H
    INT 33H
    RET            
    
COMPARACIONES:
    MOV AX,05H
    MOV BX,0000
    INT 33H
    CMP BX,0001B
    JE COMPARAR
    
COMPARAR:CMP CX,320D
    JB METODOS
    JA CONTROLES
    
CONTROLES:CMP DX,325D
    JA BTNSALIRIZQ
    JMP COMPARACIONES
    
BTNSALIRIZQ:CMP DX,370D
    JB BTNSALIRDER
    JMP COMPARACIONES
    
BTNSALIRDER:CMP CX,500D
    JA BTNSALIRARRIBA
    CMP CX,350D
    JA BTNSIGARRIBA
    JMP COMPARACIONES
    
BTNSIGARRIBA:CMP CX,450D
    JB GRAFICOBI
    JMP COMPARACIONES
    
BTNSALIRARRIBA:CMP CX,600D
    JB FIN
    JMP COMPARACIONES
    
METODOS:CMP CX,10D
    JA BTNBISECIZQ
    JMP COMPARACIONES
    
BTNBISECIZQ:CMP CX,120D
    JB BTNBISECDER
    JMP COMPARACIONES
    
BTNBISECDER:CMP DX,190D
    JA BTNPUNTOARR
    CMP DX,130D
    JA BTNNEWTONARR
    CMP DX,70D
    JA BTNBISECARR
    JMP COMPARACIONES
    
BTNPUNTOARR:CMP DX,220D
    JB FIN
    JMP COMPARACIONES
    
BTNNEWTONARR:CMP DX,160D
    JB FIN
    JMP COMPARACIONES
    
BTNBISECARR:CMP DX,100D
    JB FIN
    JMP COMPARACIONES    
    
    RET
    
