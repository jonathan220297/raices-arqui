COMPARACIONES:
    MOV AX,05H
    MOV BX,0000
    INT 33H
    CMP BX,0001B
    JE COMPARAR
    
COMPARAR:CMP CX,320D
    JB METODOS
    JA CONTROLES
    
CONTROLES:CMP DX,325D
    JA BTNSALIRIZQ
    JMP COMPARACIONES
    
BTNSALIRIZQ:CMP DX,370D
    JB BTNSALIRDER
    JMP COMPARACIONES
    
BTNSALIRDER:CMP CX,500D
    JA BTNSALIRARRIBA
    CMP CX,350D
    JA BTNSIGARRIBA
    JMP COMPARACIONES
    
BTNSIGARRIBA:CMP CX,450D
    JB FIN
    JMP COMPARACIONES
    
BTNSALIRARRIBA:CMP CX,600D
    JB FIN
    JMP COMPARACIONES
    
